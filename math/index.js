const add = (val1, val2) => {
  return val1 + val2;
};

const subtract = (val1, val2) => {
  return val1 - val2;
};

const multiply = (val1, val2) => {
  return val1 * val2;
};

const divide = (val1, val2) => {
  return val1 / val2;
};

module.exports = {
  add,
  subtract,
  multiply,
  divide,
};
